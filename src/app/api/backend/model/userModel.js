import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
    },
    imgURL: {
        type: String
    }
});

const Users = mongoose.model("Users", userSchema);

module.exports = { Users }